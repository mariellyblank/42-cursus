#include "libft.h"

char *ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	char    		*str;
	unsigned int	i;

	i = 0;
	if(!s || !f)
		return (NULL);
	str = ft_strdup(s);
	if (!str)
		return (NULL);
	while (str[i])
	{
		str[i] = f(i, str[i]);
		i++;
	}
	str[i] = '\0';
	return (str);
}
